package com.tttn.quanlynhansu.dto;

public class ChamCongDTO {
	private String id;
	private String gio_vao;
	private String gio_ra;
	private String ngay;
	private String gio_lam_them;
	private String trang_thai;
	private String nhan_vien_id;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getGio_vao() {
		return gio_vao;
	}
	public void setGio_vao(String gio_vao) {
		this.gio_vao = gio_vao;
	}
	public String getGio_ra() {
		return gio_ra;
	}
	public void setGio_ra(String gio_ra) {
		this.gio_ra = gio_ra;
	}
	public String getNgay() {
		return ngay;
	}
	public void setNgay(String ngay) {
		this.ngay = ngay;
	}
	public String getGio_lam_them() {
		return gio_lam_them;
	}
	public void setGio_lam_them(String gio_lam_them) {
		this.gio_lam_them = gio_lam_them;
	}
	public String getTrang_thai() {
		return trang_thai;
	}
	public void setTrang_thai(String trang_thai) {
		this.trang_thai = trang_thai;
	}
	public String getNhan_vien_id() {
		return nhan_vien_id;
	}
	public void setNhan_vien_id(String nhan_vien_id) {
		this.nhan_vien_id = nhan_vien_id;
	}
	
}
